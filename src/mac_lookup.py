from .config import Config

class MACLookup:
    """MAC address lookup and manufacturer identification"""
    
    def __init__(self, config: Config):
        self.config = config
        self.cache = {}
        self.api_urls = [
            "https://api.macvendors.com",
            "https://api.macaddress.io/v1"
        ]
        self.vendor_database = self._load_vendor_database()
    
    async def get_manufacturer(self, mac_address: str) -> Optional[str]:
        """Get manufacturer for MAC address"""
        
        if not mac_address:
            return None
        
        # Clean MAC address
        mac = self._clean_mac(mac_address)
        if not mac:
            return None
        
        # Check cache first
        if mac in self.cache:
            return self.cache[mac]
        
        # Try multiple API sources
        manufacturer = await self._query_apis(mac)
        if manufacturer:
            self.cache[mac] = manufacturer
            return manufacturer
        
        # Fallback to local database
        manufacturer = self._local_lookup(mac)
        if manufacturer:
            self.cache[mac] = manufacturer
            return manufacturer
        
        return None
    
    async def _query_apis(self, mac: str) -> Optional[str]:
        """Query multiple MAC vendor APIs"""
        
        for api_url in self.api_urls:
            try:
                async with aiohttp.ClientSession() as session:
                    url = f"{api_url}/{mac}"
                    async with session.get(url, timeout=3) as response:
                        if response.status == 200:
                            manufacturer = await response.text()
                            if manufacturer and manufacturer != "Not Found":
                                return manufacturer.strip()
            except Exception:
                continue
        
        return None
    
    def _clean_mac(self, mac: str) -> Optional[str]:
        """Clean and validate MAC address"""
        
        # Remove separators and convert to uppercase
        mac = re.sub(r'[.:-]', '', mac.upper())
        
        # Validate format
        if re.match(r'^[0-9A-F]{12}$', mac):
            return mac[:6]  # Return OUI (first 6 characters)
        
        return None
    
